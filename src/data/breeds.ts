/** 通过 ID 快速查找品种 */
export const BREED_MAP = new Map(BREEDS.map((b) => [b.id, b]));

/** 通过维度组合查找品种 */
export function getBreedByDimensions(
  energyLevel: Breed['energyLevel'],
  strategyLevel: Breed['strategyLevel'],
  core: Breed['core'],
): Breed | undefined {
  return BREEDS.find(
    (b) =>
      b.energyLevel === energyLevel &&
      b.strategyLevel === strategyLevel &&
      b.core === core,
  );
}
